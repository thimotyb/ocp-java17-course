package ch01.examples;

/**
 * Demonstrates the basic structure of a Java class with a main() method.
 * This is the canonical "Hello World" program showing:
 * - The public static void main(String[] args) entry point signature
 * - How a Java program begins execution
 * - The minimal structure required to compile and run a Java program
 *
 * From Chapter 1: Writing a main() Method
 */
public class Zoo {

    public static void main(String[] args) {
        System.out.println("Hello World");
    }
}
